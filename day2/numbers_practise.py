# Declare variables:
age = 25
height = 1.75   # metres
complex_number = 2 + 3j

# Print types
print("Age:", age, "| Type:", type(age))
print("Height:", height, "| Type:", type(height))
print("Complex_number:", complex_number, "| Type:", type(complex_number))

# Basic mathematics
sum_ = age + height
difference = age - height
product = age * 2
division = age / 2

print(f"Sum: {sum_} Type of Sum: {type(sum_)}, Difference: {difference} Type of Difference: {type(difference)}, Product: {product} Type of Product: {type(product)} and Division: {division} Type of Division: {type(division)}")

