# Apply mathematical operations

x = 10
y = 3

print(x + y)    # Addition
print(x - y)    # Subtraction
print(x * y)    # Multiplication
print(f"No decimal here: {x / y}")  # Division without a decimal (WHOLE NUMBER)
print("Decimal with 1 digit after the comma (x / y):", x / y)   # Division with a decimal
print("Decimal with 1 digit after the comma (y / x):", y / x)   # Division with a decimal
print("Floor Division:", x / y)     # Floor division
print("Modulus / Remainder:", x % y)    # Modulus
print("Exponentiation (x to the exponent y):", x ** y)  # Modulus (Remainder)

