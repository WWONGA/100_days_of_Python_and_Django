x = 10
print("Initial x (value):", x)      # 10

x += 5
print("x = x + 5:", x)              # 15

x -= 3
print("x = x - 3:", x)              # 12

x *= 2
print("x = x * 2:", x)              # 24

x /= 4
print("x = x / 4:", x)              # 6.0

x %= 3
print("x = x % 3:", x)              # 0.0

x **= 2
print("x = x ** 2:", x)             # 0.0