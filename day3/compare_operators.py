# Comparison operators
"""
Used to compare two variables, 
return True or False
"""
a = 5
b = 10

print(f"False - a is not equals to b: {a == b}")   # False
print(f"True - a is not equals to b: {a != b}")   # True
print(f"False - a is not greater than b: {a > b}")    # False
print(f"True - a is less than b: {a < b}")    # True
print(f"False - a is not GREATER THAN or Equals b {a >= b}")  # False
print(f"True - a is LESS THAN b, we have first TEST the first operand, which is TRUE in this case: {a <= b}")   # True

